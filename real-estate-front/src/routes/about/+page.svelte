<h1 class="text-center text-blue-500 mt-20 mb-5">About RES</h1>
<p class="px-10">
  <span class="text-blue-500">RES</span>, real estate listing app built with
  Axios, SvelteKit, and Strapi offers a modern and efficient way to browse,
  manage, and post property listings. This application leverages SvelteKit for
  its front-end, providing a reactive and highly performant user experience.
  Axios is used for making HTTP requests, seamlessly communicating with a
  Strapi-based backend. Strapi, a headless CMS, acts as the application's
  back-end server, providing a flexible and powerful API for real estate data
  management.
</p>
<p class="px-10">
  The application supports user roles, where realtors can post and manage their
  listing.
</p>
